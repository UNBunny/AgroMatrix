-- Агрономическое обоснование:
--   Бобовые культуры (горох, соя) фиксируют атмосферный азот через симбиоз
--   с Rhizobium spp. После уборки в почве остаётся 40–80 кг/га минерального N
--   в виде разлагающихся корневых остатков и узелков.
--   Это снижает потребность следующей культуры в азотных удобрениях на 30–50%.
--
--   Источники: ФГБНУ СИБНИИСХ, Агрохимия (Минеев, 2017), ВНИИ кормов.

-- Горох (8) как предшественник — оставляет 40–60 кг N/га
UPDATE crop_rotation_rules
SET nitrogen_balance = 'Горох оставляет 40–60 кг N/га: экономия азотных удобрений ~1500–2500 руб/га'
WHERE predecessor_crop_id = 8
  AND successor_crop_id IN (1, 2, 3, 4, 5, 7, 9, 10);

-- Соя (6) как предшественник — оставляет 60–80 кг N/га
UPDATE crop_rotation_rules
SET nitrogen_balance = 'Соя оставляет 60–80 кг N/га: экономия азотных удобрений ~2000–3500 руб/га'
WHERE predecessor_crop_id = 6
  AND successor_crop_id IN (1, 2, 3, 4, 5, 9, 10);

-- Рапс (7) как предшественник — рыхлит почву, небольшой остаточный N
UPDATE crop_rotation_rules
SET nitrogen_balance = 'Рапс улучшает структуру почвы, оставляет ~15–20 кг N/га из соломы'
WHERE predecessor_crop_id = 7
  AND successor_crop_id IN (1, 2, 3, 4);

-- Гречиха (10) — нейтральный предшественник, мобилизует фосфор
UPDATE crop_rotation_rules
SET nitrogen_balance = 'Гречиха мобилизует труднодоступные фосфаты почвы — снижает потребность в P-удобрениях'
WHERE predecessor_crop_id = 10
  AND successor_crop_id IN (1, 2, 3);
